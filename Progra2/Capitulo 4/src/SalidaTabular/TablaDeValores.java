public class TablaDeValores {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
            for (int j = 1; j <= 4; j++) {
                int valor = i * (int) Math.pow(10, j);
                System.out.printf("%6d%n", valor);
            }
        }
    }
}
